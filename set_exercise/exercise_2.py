# Return a new set of identical items from two sets
# Given:
# set1 = {10, 20, 30, 40, 50}
# set2 = {30, 40, 50, 60, 70}
set1 = {10, 20, 30, 40, 50}
set2 = {30, 40, 50, 60, 70}

# Find the set of identical items using the intersection() method
identical_items = set1.intersection(set2)

# Print the set of identical items
print(identical_items)

